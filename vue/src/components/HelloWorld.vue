<template>
  <div class="hello">
    <h1>{{ msg }}</h1>
    <h2>Essential Links</h2>
    <h2>{{ rocketMsg }}</h2>
  </div>
</template>

<script>
import axios from 'axios'

export default {
  name: 'HelloWorld',
  data () {
    return {
      msg: 'Welcome to Your Beautiful Vue.js App: Let\'s spread code around the world!',
      rocketMsg: ''
    }
  },
  mounted() {
    axios.get('http://localhost:8000/hello')
      .then(result => {
        this.rocketMsg = result.data.message
      })
      .catch(error => {
        console.log(error)
      })
  }
}
</script>

